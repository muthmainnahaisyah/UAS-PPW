            <h5>SEJARAH</h5>
            <h2>CITRA NIAGA SAMARINDA</h2>
            <p>
            Citra Niaga merupakan kawasan pusat perdagangan di Kota Samarinda, Kalimantan Timur dengan luas sekitar 2,7 hektare yang dirancang untuk menyediakan tempat usaha bagi pedagang kecil (60%) 
            serta pedagang besar dan menengah (40%). Citra Niaga dibangun pada tanggal 27 Agustus 1987. Lokasinya adalah bekas tempat bernama Taman Hiburan Gelora yang terbakar. Kondisi saat itu jumlah 
            penduduk Samarinda baru sekitar 300.000 orang dan memerlukan pusat belanja dan rekreasi. Awal dari alasan kawasan ini didirikan adalah sebagai peremajaan kawasan sekitar, yang kemudian 
            diinterpetasikan sebagai usaha untuk mengubah citra dan wajah salah satu kawasan tua, kumuh, padat, buruk, kotor, tidak terukur di Samarinda menjadi kawasan perdagangan baru yang bersih, 
            fungsional dan berestetika. <br><br>

            Sebagian bangunan Citra Niaga di sisi timur telah mengalami perubahan fungsi. Bangunan berlantai dua dengan struktur permanen tersebut awalnya memiliki gang (alley) di bagian dalam yang bisa dilalui 
            pengunjung dengan penghawaan alami. Selanjutnya seluruh bagian tersebut ditutup dan berubah menjadi sebuah department store yang tertutup. Menyisakan bagian barat untuk pedagang kecil dan plaza di tengah.
            Citra Niaga mengalami kebakaran pada 31 Oktober 2006 dan kemudian dibangun kembali namun tidak persis sama dengan kondisi awal dibangun dan merupakan pusat kerajinan tradisional di kota Samarinda. Perubahan 
            terjadi pada bahan bangunan dimana sebelumnya seluruh bahan dinding dan atap dari sirap kayu ulin, diganti dengan bahan-bahan non kayu dan non kayu ulin.
            </p>
